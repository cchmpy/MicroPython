from array import array
a = array('I',[300,400,500,600,700])
b = array('i',[3000,-4000,5000])
c = array('b',[30,40,50])
a[2] = 1000         # 索引赋值，右操作数不能是bytes，如b'\x01\x02\x00\x00'
a[:2] = b[1:]       # 切片赋值，右操作数不能是bytes，可以是4字节类型'i'、'I'、'l'、'L'的array
print(a)            # 输出：array('I', [4294963296, 5000, 1000, 600, 700])
c[:1] = b'\x01\x02' # 切片赋值
print(c)            # 输出：array('B', [1, 2, 40, 50])
